def resolve_enterprise_template(template_key):
    key = str(template_key or '').strip() or 'generic_linux_net_snmp'
    if key not in ENTERPRISE_TEMPLATES:
        key = 'generic_linux_net_snmp'
    layered = copy.deepcopy(ENTERPRISE_TEMPLATES[key])
    if not isinstance(layered.get('oids'), dict):
        layered['oids'] = {}
    merged_macros = dict(_DEFAULT_MACRO_SEED)
    if isinstance(layered.get('macros'), dict):
        merged_macros.update(layered['macros'])
    layered['macros'] = merged_macros
    if not isinstance(layered.get('value_maps'), dict):
        layered['value_maps'] = {}
    if not isinstance(layered.get('value_map_sources'), dict):
        layered['value_map_sources'] = {}
    return layered


